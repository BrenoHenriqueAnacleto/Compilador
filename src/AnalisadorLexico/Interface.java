    public void analiseLexica() throws IOException {
        File arquivo = new File("arquivo.txt");
        PrintWriter writer;
        try{
            writer = new PrintWriter(arquivo);
            writer.print(jTextField1.getText());
            writer.close();
        }catch (FileNotFoundException ex){
            Logger.getLogger(Interface.class.getName()).log(Level.SEVERE,null,ex);
        }
        Reader reader = new BufferedReader(new FileReader("arquivo.txt"));
        GameController  lexer = new GameController(reader);
        String resultado = "";
        while(true){
            Token token = lexer.yylex();
            if(token == null){
                resultado = resultado + "EOF";
                jTextField2.setText(resultado);
                return;
            }
            switch (token){
                case ERROR:
                    resultado = resultado + "Erro,simbolo n√£o reconhecido";
                    break;
                case ID: case INT:
                    resultado = resultado + "Token " + token + " " +lexer.lexeme + "\n";
                    break;
                default:
                    resultado = resultado + "Token" + token + "\n";
            }
        }
    }
    
    
